#!/usr/bin/env python3
"""
This module provides a function 'add' that takes two floats and returns their sum.

Author: Your Name
"""

def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns their sum.

    :param a: The first float number.
    :param b: The second float number.
    :return: The sum of 'a' and 'b' as a float.
    """
    return a + b
